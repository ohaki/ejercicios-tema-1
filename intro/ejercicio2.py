# Definición de variables
numero_positivo = 10
numero_negativo = -5
saludo = "Hola "
lista_numeros = [1, 2, 3]

# Operaciones con números
print(numero_positivo * 5)  # Multiplicación: 50
print(numero_positivo - numero_negativo)  # Resta: 15

# Operaciones con cadenas
print(saludo + "Mundo")  # Concatenación: "Hola Mundo"
print(saludo * 2)  # Repetición: "Hola Hola "

# Operaciones con listas
print(lista_numeros[-1])  # Último elemento: 3
print(lista_numeros[1:])  # Sublista desde el segundo elemento: [2, 3]
print(lista_numeros + lista_numeros)  # Concatenación de listas: [1, 2, 3, 1, 2, 3]
